# learn to use the turtle and math libraries

import turtle
import math
x = 50
c = math.sqrt(x**2 + x**2)

turtle.shape("turtle")
turtle.forward(x)
turtle.left(90)
turtle.forward(x)
turtle.left(90)
turtle.forward(x)
turtle.left(90)
turtle.forward(x)
turtle.left(135)
turtle.forward(c)
turtle.left(90)
turtle.forward(c/2)
turtle.left(90)
turtle.forward(c/2)
turtle.left(90)
turtle.forward(c)
turtle.left(45)
turtle.exitonclick()
